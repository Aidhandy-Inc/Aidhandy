import { serve } from "https://deno.land/std@0.177.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    const { offer_id } = await req.json();

    if (!offer_id) {
      return new Response(
        JSON.stringify({ error: 'Missing offer_id' }),
        { 
          status: 400, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      );
    }

    const DUFFEL_API_URL = `https://api.duffel.com/air/seat_maps?offer_id=${offer_id}`;
    const DUFFEL_TOKEN = Deno.env.get('DUFFEL_SANDBOX_TOKEN');

    if (!DUFFEL_TOKEN) {
      return new Response(
        JSON.stringify({ error: 'Duffel token not configured' }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      );
    }

    const response = await fetch(DUFFEL_API_URL, {
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${DUFFEL_TOKEN}`,
        'Accept': 'application/json',
        'Duffel-Version': 'v2',
      },
    });

    const data = await response.json();

    if (!response.ok) {
      return new Response(
        JSON.stringify({ 
          error: 'Duffel API error', 
          details: data 
        }),
        { 
          status: response.status, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      );
    }

    return new Response(
      JSON.stringify(data),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );

  } catch (error) {
    console.error('Edge function error:', error);
    return new Response(
      JSON.stringify({ error: 'Internal server error', details: error.message }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  }
});
